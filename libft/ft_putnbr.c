#include "libft.h"

void	ft_putnbr(int n)
{
	int k;

	k = n;
	if (k < -2147483647)
	{
		ft_putchar('-');
		ft_putchar('2');
		k = 147483648;
	}
	if (k < 0)
	{
		ft_putchar('-');
		k = k * -1;
	}
	if (k >= 10)
	{
		ft_putnbr(k / 10);
	}
	ft_putchar(k % 10 + '0');
}
